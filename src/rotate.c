#include "push_swap.h"

int	rotate(t_list **stack)
{
	t_list	*head;
	t_list	*end;

	if (ft_lstsize(*stack) < 2)
		return (-1);
	head = *stack; // La cabeza de la lista es head
	end = ft_lstlast(*stack); // El final de la lista es end
	*stack = head->next;
	head->next = NULL;
	end->next = head; // Pasamos el Ãºltimo nodo al head
	return (0);
}

int	ra(t_list **stack_a)
{
	if (rotate(stack_a) == -1)
		return (-1);
	ft_putendl_fd("ra", 1);
	return (0);
}

int	rb(t_list **stack_b)
{
	if (rotate(stack_b) == -1)
		return (-1);
	ft_putendl_fd("rb", 1);
	return (0);
}

int	rr(t_list **stack_a, t_list **stack_b)
{
	if ((ft_lstsize(*stack_a) < 2) || (ft_lstsize(*stack_b) < 2))
		return (-1);
	rotate(stack_a);
	rotate(stack_b);
	ft_putendl_fd("rr", 1);
	return (0);
}